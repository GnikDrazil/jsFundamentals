//EQUAL
'3' == 3;
console.log('3' == 3);

// STRICT EQUAL - overides javascript coercion
console.log(3 === 3); // true
console.log ('3' === 3);

// NOT EQUAL
console.log('3' != 3);

//STRICT NOT EQUAL
console.log('3' !== 4);
console.log(4 !== 5);

//GREATER THAN
3 > 2

//LESS THAN
2 < 3

//GREATER THAN OR EQUAL TO
3 >= 2;

//LESS THAN OR EQUAL TO 
2 <= 3;

// AND
2 && 3

// OR

2 || 3;
